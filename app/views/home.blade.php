<h2>Welcome to HERoes In STEM</h2>
<br/><br/>
<h3>What is this place?</h3>
<br/>
<p class="info "> HERoes in STEM is a site dedicated to letting users find their Lady STEM heroes, allowing users to share their STEM accomplishments, and increasing visibilty of STEM role models from history, fiction, and the present.</p>
<br/>
<h3>Why is this place?</h3>
<br/>
<p class="info"> I've been told that there are no good role models for girls in STEM and that's just a lie. There are plenty of role models, you just have to look for them! Just because you aren't taught about them in school, doesn't mean they don't exist. :) </p>
<br/>
<h3>What do I do in this place?</h3>
<br/>
<p class="info "> Search for your future HERoes, add a HERo, list the accomplishments that are helping you to become a future HERo, and tell other users who you think are awesome you admire them. </p>
<br/>
<h3>Why can I admire other users? I thought this was for finding role models?</h3>
<br/>
<p class="info ">Well, this site (not so secretly) has two goals. As you can see in this <a href='http://en.wikipedia.org/wiki/Women_in_STEM_fields#Social-psychological_interventions'>informative wiki article about women in STEM feilds</a>, there are two main types of social-psychological interventions that can be used to combat <a href="http://en.wikipedia.org/wiki/Women_in_STEM_fields#Stereotype_threat">stereotype threat</a>, introducing role models and self-affirmation. While this site can't replace a role model you know personally, it does increase your knowlege of the great number of important women in STEM feilds. </p> 
<p class="info ">The second intervention, self-affirmation, comes into play when you write about yourself in your profile. Users just writing about what they are good at and their accomplishments has a positive effect. </p>
<p class="info ">We'd like to take it a step further. While another user may not be a full-fledged famous woman in STEM yet, they can still be pretty awesome and be doing some pretty admirable things.  </p>
<p class="info">What better way to boost thier confidence then to have you tell them? Everyone starts somewhere, and even little steps can be amazing. Users telling other users when they admire them for their little steps can lead to some more little steps and possibly a REALLY big step.  </p>
	Let's cheer each other on as we become the best STEM girls we can be! </p>
