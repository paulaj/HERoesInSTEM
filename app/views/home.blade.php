<h3>What is this place?</h3>
<p> HERoes in STEM is a site dedicated to letting users find their Lady STEM heroes and increasing visibilty of STEM role models from history, fiction, and the present.</p>

<h3>Why is this place?</h3>
<p> I've been told that there are no good role models for girls in STEM and that's just a lie. There are plenty of role models, you just have to look for them!  </p>

<h3>What do I do in this place?</h3>
<p> Search for your future HERoes, add a HERo, list the accomplishments that are helping you to become a future HERo, and tell other users who you think are awesome you admire them. </p>

<h3>Why can't I friend other users? Why can I only admire them?</h3>
<p>  While another user may not be a full-fledged famous woman in STEM yet, they can still be pretty awesome and be doing some pretty admirable things. 
	I'd like users to tell each other when their doing something admireable. Everyone starts somewhere, and even little steps can be amazing. Also, it's hard to be someone's friend when you don't know them. You should go to facebook for that. :) </p>
